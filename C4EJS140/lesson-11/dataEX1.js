function getRandomProductImg(product) {
    let randomIndex = Math.floor(Math.random() * (product.img.length));
    while(product.img[randomIndex]==undefined) {
        let randomIndex = Math.floor(Math.random() * (product.img.length));
    }
    return product.img[randomIndex];
}
document.addEventListener("DOMContentLoaded", function() {
    const productListElement = document.getElementById("productList");
    for(let i=0;i<storedProducts.length;i++) {
    const productElement = document.createElement('div');
    productElement.style.textAlign="center";
    productElement.onclick=function() {
        window.location.href = `subbaitap-1.html?product=${JSON.stringify(storedProducts[i])}`;
    }
    const imgElement = document.createElement('img');
    imgElement.src = getRandomProductImg(storedProducts[i]);
    
    const nameElement = document.createElement('p');
    nameElement.textContent = storedProducts[i].nameProduct;

    const priceElement = document.createElement('p');
    priceElement.textContent = 'Price: $'+storedProducts[i].price;
    const discountElement = document.createElement('p');
    if(storedProducts[i].discount!=""){
        discountElement.textContent = storedProducts[i].discount+'%';
    }
    discountElement.style.color="red";
    discountElement.style.fontWeight="bold";
    productElement.appendChild(discountElement);
    productElement.appendChild(imgElement);
    productElement.appendChild(nameElement);
    productElement.appendChild(priceElement);
    
    productListElement.appendChild(productElement);
    };
  });

