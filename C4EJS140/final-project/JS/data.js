    nameElement.textContent = storedProducts[i].nameProduct;

    const priceElement = document.createElement("p");
    priceElement.textContent = "Price: $" + storedProducts[i].price;
    const discountElement = document.createElement("p");
    const addToCart = document.createElement("button");
    addToCart.textContent = "Mua hàng";
    addToCart.classList.add("button");
    addToCart.classList.add("addToCart");
    const productDetails = document.createElement("button");
    productDetails.textContent = "Chi tiết sản phẩm";
    productDetails.classList.add("button");
